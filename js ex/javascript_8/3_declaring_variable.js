// variable 선언 하기
// 1) var - 더이상 쓰지않는다,
// 2) let - 
// 3) cosnt - 
var name = '코드팩토리';
console.log(name);

var age= 32;
console.log(age);

let ive = '아이브';
console.log(ive);

// let , var 로 선언하면 값을 추후 변경할수 있다.
ive = '안유진';
console.log(ive);

const newjeans = '뉴진스';
console.log(newjeans);

// newjeans = '코드';

