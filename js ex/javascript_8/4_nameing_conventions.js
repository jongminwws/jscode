/* naming conventions
 변수 이름 지을 때 
  1) 일반적으로 영어(라틴어)를 사용하여 문자와 숫자를 모두 사용 할 수 있다,
  2) 특수기호는 언더스코어와 달러표시만 사용할수 있다. 
  3) 숫자로 이름을 시작할수 없다.
      1name, 2name
  4) 키워드는 변수명으로 사용할수 없다,
     var = 'var'; 안됨
     */

let codefactory = '코드 팩토리';
var $ive = 'ive';
const _newjeans = '뉴진스';

console.log(codefactory);
console.log($ive);
console.log(_newjeans);

